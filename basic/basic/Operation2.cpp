#include <iostream>
using namespace std;


int main_operation() {


	//비교 연산 - 결과값(true = 1 / false = 0)
	int a = 99;
	int b = 9;


 

	//논리 연산 - && 그리고    || 또는            ! 부정
	//			  둘 다 참     둘 다 거짓 

	cout << ((a == b) && (a > b)) << endl;
	cout << ((a == b) || (a > b)) << endl << endl << endl << endl;
	cout << !(a > b) << endl;












	return 0;
}
